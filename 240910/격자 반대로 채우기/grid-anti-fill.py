n = int(input())

a = [
    [0 for i in range(n)]
    for i in range(n)
]

cnt = 1
for i in range(n - 1, -1, -1):
    for j in range(n):
        if (n - i) % 2 == 0:  # 홀수 번째 열 (오른쪽에서 부터)에서는 위에서 아래로
            a[j][i] = cnt
        else:  # 짝수 번째 열에서는 아래에서 위로
            a[n - 1 - j][i] = cnt
        cnt += 1

for i in range(n):
    for j in range(n):
        print(a[i][j], end=' ')
    print()


